#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		l;
	int		i;

	l = ft_strlen(s1);
	i = 0;
	str = (char *)malloc((l + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
